
public class operadoresDeAsignacion {
    public static void main(String args[]) {
        int a = 3, b = 2;
        int c = a + 5 - b;
        System.out.println("c = " + c);
        
        a += 1; //Esto significa a = a + 1 incrementamos
        System.out.println("a = " + a);
        
        a += 3; //a = a + 3
        System.out.println("a = " + a);
        
        a -= 2; // a = a - 2 significa
        System.out.println("a = " + a);
        
        // los operadores a usar son *= /= %= +/ -/
    }
    
}
